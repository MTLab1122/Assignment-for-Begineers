"use strict";
//Beginner 1: Create an array and print its content.
const names = ['Khan', 'Jan', 'Shakir', 'Abdullah'];
console.log(`The content of this array is: ${names}`);
//Beginner 2: Add an element to an array.
const names1 = ['Khan', 'Jan', 'Shakir', 'Abdullah'];
names1.push('Talha');
console.log(`The new array is : ${names1}`);
//Beginner 3: Remove an element from an array.
const names2 = ['Khan', 'Jan', 'Shakir', 'Abdullah'];
names2.pop();
console.log(`The array after remove an element is : ${names2}`);
//Beginner 4: Sort an array.
const names3 = ['Khan', 'Jan', 'Shakir', 'Abdullah'];
names3.sort();
console.log(names3);
